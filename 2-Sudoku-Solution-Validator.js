function validSolution(arr) {
  if (!checkAll3by3(arr)) return false;
  if (!checkAllRows(arr)) return false;
  if (!checkAllCols(arr)) return false;
  return true;
}

function checkAll3by3(arr) {
  for (let i=0; i<9; i+=3) {
    for (let j=0; j<9; j+=3) {
      if (!checkOne3by3(arr, i, j)) return false;
    }
  }
  return true;
}

function checkOne3by3(arr, row, col) {
  const nums = {};
  for (let i=row; i<row+3; i++) {
    for (let j=col; j<col+3; j++) {
      let number = arr[i][j];
      if (number === 0) return false;
      if (nums[number]) return false;
      nums[number] = true;
    }
  }
  return true;
}

function checkAllRows(arr) {
  for (let row=0; row<9; row++) {
    const nums = {};
    for (let col=0; col<9; col++) {
      let number = arr[row][col];
      if (nums[number]) return false;
      nums[number] = true;
    }
  }
  return true;
}

function checkAllCols(arr) {
  for (let col=0; col<9; col++) {
    const nums = {};
    for (let row=0; row<9; row++) {
      let number = arr[row][col];
      if (nums[number]) return false;
      nums[number] = true;
    }
  }
  return true;
}

